package in.co.rays.Bean;

public class LogInBean {
	private String name;
	private String pwd;
	
	public LogInBean() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	

}
